#!/usr/bin/env node
/**
 * Generates subpath re-export files and updates package.json exports.
 *
 * Usage:  npm run gen:subpaths
 *
 * For each entry in lib-subpaths-manifest.mjs this script:
 *   1. Writes  src/subpaths/<key>.ts  that re-exports from the barrel (../index).
 *   2. Adds a "./<key>" export condition to package.json pointing to dist/.
 */

import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { subpaths } from "./lib-subpaths-manifest.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const subpathsDir = resolve(root, "src/subpaths");
const pkgPath = resolve(root, "package.json");

mkdirSync(subpathsDir, { recursive: true });

// ── 1. Generate src/subpaths/<key>.ts ────────────────────────────────────────
for (const [key, { values = [], types = [] }] of Object.entries(subpaths)) {
  const lines = [
    `/** Auto-generated by scripts/gen-subpaths.mjs — do not edit by hand. */`,
  ];

  if (values.length > 0) {
    lines.push(`export { ${values.join(", ")} } from "../index";`);
  }
  if (types.length > 0) {
    lines.push(`export type { ${types.join(", ")} } from "../index";`);
  }

  const filePath = resolve(subpathsDir, `${key}.ts`);
  writeFileSync(filePath, lines.join("\n") + "\n", "utf8");
  console.log(`  wrote  src/subpaths/${key}.ts`);
}

// ── 2. Update package.json exports ───────────────────────────────────────────
const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));

const rootEntry = pkg.exports["."];
const cssEntry = pkg.exports["./style.css"];

const generatedExports = {};
for (const key of Object.keys(subpaths)) {
  generatedExports[`./${key}`] = {
    types: `./dist/subpaths/${key}.d.ts`,
    import: `./dist/subpaths/${key}.js`,
    require: `./dist/subpaths/${key}.cjs`,
  };
}

pkg.exports = {
  ".": rootEntry,
  "./style.css": cssEntry,
  ...generatedExports,
};

writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + "\n", "utf8");
console.log(`  updated package.json exports (${Object.keys(generatedExports).length} subpaths)`);
console.log("\nDone. Commit src/subpaths/*.ts and package.json together.");
